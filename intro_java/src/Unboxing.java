public class Unboxing {
    public static void main(String[] args) {
        Integer x=10;
        Float y=1.9f;
        Character z='C';

        int i=x;
        float f=y;
        char c=z;

        System.out.println(x);
        System.out.println(i);
        System.out.println(f);
        System.out.println(c);
    }
}
